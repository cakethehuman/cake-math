package cake

func Add(a, b int) int {
	return a + b
}

func Subtract(a, b int) int {
	return a - b
}

func devide(a, b int) int {
	return a / b
}

func Multiply(a, b int) int {
	return a * b
}

func Modulus(a, b int) int {
	return a % b
}

func power(a, b int) int {
	return a * b
}

func sqrt(a float32) float32 {
	return a * 0.5
}
